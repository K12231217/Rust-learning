//! 统一配置入口 —— 固件身份 + 可调行为策略。
//!
//! 职责边界(量产经验:文件边界 = 改它的人的边界):
//! - **本文件** 放「固件/产品负责人」关心、且想集中调的东西:版本号、各状态闪烁周期、
//!   心跳间隔。PM、测试、三个月后的你想一眼找到它们。
//! - **硬件事实**(晶振频率、时钟、引脚、芯片/板型)不在这里,在 [`crate::board`]。
//!   那些跟「这块板子是什么」绑定,改版时和 PLL 配置一起改,放一起才不会改漏。
//!
//! 判断一个常量该放哪:被多模块共享 / 需要集中调 → 这里;只有 board 用 → board.rs。

// ───── 固件身份信息 ─────

/// 固件版本号。直接取 Cargo.toml 的 `version`,改版本只改一处。
pub const FW_VERSION: &str = env!("CARGO_PKG_VERSION");

// ───── 行为策略(闪烁周期,供 app.rs 使用)─────

/// 正常运行(心跳灯)半周期:每 1000ms 翻转一次 LED。
pub const LED_RUNNING_MS: u64 = 1000;

/// 启动阶段:快闪,提示「正在初始化」。
pub const LED_BOOTING_MS: u64 = 150;

/// 错误状态:250ms 快闪,提示「出问题了」。
pub const LED_ERROR_MS: u64 = 250;

/// 心跳日志打点周期(不占用任何硬件)。
pub const HEARTBEAT_MS: u64 = 1000;
